package code_ptit;
import java.util.Scanner;
import java.util.Set;
import java.util.TreeSet;
public class J02006_hopcuahaidayso {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();
        Set<Integer> hop = new TreeSet<>();
        while(n-- > 0){
            int x = sc.nextInt();
            hop.add(x);
        }
        while(m-- > 0){
            int x = sc.nextInt();
            hop.add(x);
        }
        for(int it : hop)
            System.out.print(it + " ");
    }
}
